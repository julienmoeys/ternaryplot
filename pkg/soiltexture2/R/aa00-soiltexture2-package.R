#'  classify and transform soil textures data. These
#'  functions allows to plot any soil texture triangle
#'  (classification) into different triangle geometries
#'  (isosceles, right-angled triangles, etc.). This set
#'  of function is expected to be useful to people using
#'  soil textures data from different soil texture
#'  classification or different particle size systems.
#'  Many (> 15) texture triangles from all around the
#'  world will be predefined in the package. 'soiltexture2'
#'  is a complete redesign and re-documentation of the
#'  soiltexture-package, from which is keeps the core features.
